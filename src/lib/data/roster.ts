function teamPalette(teamName: string) {
  return teamMap.get(teamName)?.hexPalette ?? ["#111111", "#d1d5db", "#f4efe8"];
}

function teamColors(teamName: string) {
  return teamMap.get(teamName)?.colors ?? ["Black", "Silver"];
}

function teamSlug(teamName: string) {
  return teamMap.get(teamName)?.slug ?? "team";
}

