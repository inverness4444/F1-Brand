const teamMap = new Map(teams.map((team) => [team.name, team] as const));
const driverMap = new Map(drivers.map((driver) => [driver.name, driver] as const));
const legendMap = new Map(legends.map((legend) => [legend.name, legend] as const));

const dateBase = new Date("2026-02-01T09:00:00.000Z");

function seededDate(index: number, badge: ProductBadge) {
  const nextDate = new Date(dateBase);
  nextDate.setDate(nextDate.getDate() + ((index * 5) % 88));
  if (badge === "New") {
    nextDate.setDate(nextDate.getDate() + 25);
  }
  return nextDate.toISOString();
}

function seededPopularity(index: number, badge: ProductBadge, boost = 0) {
  const base = 58 + ((index * 13) % 31);
  if (badge === "Hit") {
    return Math.min(99, base + 16 + boost);
  }
  if (badge === "Limited") {
    return Math.min(96, base + 8 + boost);
  }
  return Math.min(94, base + boost);
}

function getCollectionTags(collection: FeaturedCollection, badge: ProductBadge, createdAt: string) {
  const tags = new Set<FeaturedCollection>([collection]);
  if (badge === "Sale") {
    tags.add("Sale");
  }
  if (badge === "New" || createdAt >= "2026-04-01") {
    tags.add("New Arrivals");
  }
  return [...tags];
}

export function sanitizeProductDescription(description: string) {
  return description.trim().replace(/\s+/g, " ");
}

export function createDefaultProductDescription({
  name,
  category,
}: Pick<Product, "name" | "category">) {
  const collectionText =
    category === "Pilots"
      ? "driver collection"
      : category === "Teams"
        ? "teamwear collection"
        : category === "Legends"
          ? "legends capsule"
          : "performance essentials";

  return `${name} is part of the ${collectionText} and combines clean athletic styling with premium motorsport-inspired detailing. Built for everyday wear with a polished sportswear finish, it sits comfortably between team apparel and elevated lifestyle performance clothing.`;
}

function makeGallery(type: ProductType, palette: string[]) {
  return [0, 1, 2, 3].map((variant) => createProductMockup(type, palette, variant));
}

function buildProduct(seed: ProductSeed, index: number) {
  const team = seed.teamName ? teamMap.get(seed.teamName) : undefined;
  const driver = seed.driverName ? driverMap.get(seed.driverName) : undefined;
  const legend = seed.legendName ? legendMap.get(seed.legendName) : undefined;
  const palette =
    driver?.hexPalette ??
    team?.hexPalette ??
    legend?.hexPalette ??
    ["#111111", "#d7c8b7", "#ece4d7"];
  const colors = driver?.colors ?? team?.colors ?? legend?.colors ?? ["Black", "Beige"];
  const createdAt = seed.createdAt ?? seededDate(index, seed.badge);
  const popularity = seededPopularity(index, seed.badge, seed.popularityBoost ?? 0);
  const id = slugify(
    [
      seed.driverName ?? seed.teamName ?? seed.legendName ?? seed.collection,
      seed.name,
      seed.type,
    ].join("-"),
  );
  const gallery = makeGallery(seed.type, palette);

  return {
    id,
    slug: id,
    name: seed.name,
    category: seed.category,
    collection: seed.collection,
    collectionTags: seed.collectionTags ?? getCollectionTags(seed.collection, seed.badge, createdAt),
    driverName: seed.driverName ?? null,
    driverSlug: driver?.slug ?? null,
    teamName: seed.teamName ?? null,
    teamSlug: team?.slug ?? driver?.teamSlug ?? null,
    legendName: seed.legendName ?? null,
    legendSlug: legend?.slug ?? null,
    gender: seed.gender,
    price: seed.price,
    productType: seed.type === "Gift Certificate" ? "gift_certificate" : "standard",
    requiresShipping: seed.type !== "Gift Certificate",
    colors,
    sizes: sizesByType[seed.type],
    type: seed.type,
    badge: seed.badge,
    image: gallery[0],
    gallery,
    description: sanitizeProductDescription(seed.description),
    shortDescription: seed.shortDescription,
    popularity,
    createdAt,
    number: driver?.number,
    hexPalette: palette,
  } satisfies Product;
}

